def _fill_amino(class_colors: dict[str, RGBA]) -> dict[str, RGBA]:
    out: dict[str, RGBA] = {}
    for cls, residues in _AMINO_PROPERTY_CLASSES.items():
        for r in residues:
            out[r] = class_colors[cls]
    return out


