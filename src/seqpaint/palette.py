def _amino_grayscale() -> dict[str, RGBA]:
    keys = "acdefghiklmnpqrstvwy"
    out: dict[str, RGBA] = {}
    for i, k in enumerate(keys):
        v = 30 + round(i * (220 / (len(keys) - 1)))
        out[k] = (v, v, v, 0xFF)
    for extra in "bjzux*":
        out[extra] = (0x80, 0x80, 0x80, 0xFF)
    return out


_AMINO_GRAYSCALE = _amino_grayscale()


